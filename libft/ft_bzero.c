#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char *ss;

	if (n <= 0)
		return ;
	ss = (unsigned char *)s;
	while (n--)
	{
		*ss = '\0';
		ss++;
	}
}
